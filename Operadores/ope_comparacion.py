"""
==	Igual a
!=	Diferente de
>	Mayor que
<	Menor que
>=	Mayor o igual que
<=	Menor o igual que
"""

num1 = 10
num2 = 1

# Igual a
if num1 == num2:
    print(num1 + num2)

else:
    print("Los números no son iguales")


# Diferente de
if num1 != num2:
    print("Los números son diferentes")

else:
    print("Los números son iguales")


# Mayor que
if num1 > num2:
    print(f"{num1} es mayor a {num2}")

else:
    print(f"{num1} es menor a {num2}")


# Menor que
if num1 != num2:
    print("Los números son diferentes")

else:
    print("Los números son iguales")
    
    
# Mayor o igual a
if num1 >= num2:
    print(f"{num1} es mayor o igual a {num2}")

else:
    print(f"{num1} no es mayor ni igual a {num2}")
    
    
# Menor o igual a
if num1 <= num2:
    print(f"{num1} es menor o igual a {num2}")

else:
    print(f"{num1} no es menor ni igual a {num2}")