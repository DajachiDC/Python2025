# Suma: +
# Resta: -
# Multiplicación: *
# División: /
# Módulo: % (resto de la división)
# Exponente: ** (potencia)
# División entera: // (Devuelve el cociente de la división)

# Operadores aritméticos
a = 10
b = 3

suma = a + b  # 13
resta = a - b  # 7
multiplicacion = a * b  # 30
division = a / b  # 3.333...
modulo = a % b  # 1
exponente = a ** b  # 1000
division_entera = a // b  # 3